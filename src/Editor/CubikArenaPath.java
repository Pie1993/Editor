package Editor;

public class CubikArenaPath {

	public static String getModelpath() {
		return modelPath;
	}

	public static String getMappath() {
		return mapPath;
	}

	public static String getTexturespath() {
		return texturesPath;
	}

	public static String getSoundspath() {
		return soundsPath;
	}

	private final static String modelPath = "assets/Models";
	private final static String mapPath = "assets/Map";
	private final static String texturesPath = "assets/Textures";
	private final static String soundsPath = "assets/Sounds";

}
